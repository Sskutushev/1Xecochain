// server/middleware/auth.js
// 
// PURPOSE: Authentication middleware
// RESPONSIBILITY: Verifies user authentication for protected routes
// IMPLEMENTS: JWT-based authentication from specification
// 
// FUNCTIONS:
// - protect: Verifies JWT token and attaches user to request

const jwt = require('jsonwebtoken');
const User = require('../models/User');

// @desc    Protect routes
// @access  Private
const protect = async (req, res, next) => {
  let token;

  if (req.headers.authorization && req.headers.authorization.startsWith('Bearer')) {
    try {
      // Get token from header
      token = req.headers.authorization.split(' ')[1];

      // Verify token
      const decoded = jwt.verify(token, process.env.JWT_SECRET || 'fallback_secret');

      // Get user from the token (without password)
      req.user = await User.findById(decoded.id).select('-password');

      next();
    } catch (error) {
      console.error(error);
      return res.status(401).json({
        success: false,
        message: 'Not authorized, token failed'
      });
    }
  }

  if (!token) {
    return res.status(401).json({
      success: false,
      message: 'Not authorized, no token'
    });
  }
};

module.exports = { protect };